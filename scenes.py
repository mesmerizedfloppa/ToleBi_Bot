def get_scene(scene_id: int) -> dict | None:
    """Возвращает сцену по scene_id или None, если не найдена."""
    for scene in SCENES:
        if scene["scene_id"] == scene_id:
            return scene
    return None


def get_first_scene_id() -> int:
    """Возвращает ID первой сцены (минимальный scene_id)."""
    return min(s["scene_id"] for s in SCENES)


def get_total_scenes_count() -> int:
    """Общее количество сцен."""
    return len(SCENES)
